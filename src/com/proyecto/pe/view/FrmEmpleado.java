    private void btn_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AgregarActionPerformed
        // TODO add your handling code here:
         if(txt_IDEmpleado.getText().isEmpty() || txt_PuestoID.getText().isEmpty() || txt_Nombres.getText().isEmpty() || 
                txt_Apellidos.getText().isEmpty() || txt_DNI.getText().isEmpty()|| txt_Telefono.getText().isEmpty()
                ||txt_Email.getText().isEmpty()||txt_Direccion.getText().isEmpty()||txt_FecNac.getText().isEmpty()
                 ||txt_FecCont.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Llene todos los campos");
        }else{
            
        
            
             Empleado empleado = new Empleado();
             empleado.setEmpleado_ID(Integer.parseInt(txt_IDEmpleado.getText()));
             empleado.setPuesto_ID(Integer.parseInt(txt_PuestoID.getText()));
             empleado.setNombres(txt_Nombres.getText());
             empleado.setApellidos(txt_Apellidos.getText());
             empleado.setDni(txt_DNI.getText());
             empleado.setTelefono(txt_Telefono.getText());
             empleado.setEmail(txt_Email.getText());
             empleado.setDireccion(txt_Direccion.getText());
             empleado.setFechaNac(txt_FecNac.getText());
             empleado.setFechaContr(txt_FecCont.getText());
             
           
            String mensaje = ebo.agregarEmpleado(empleado);
            JOptionPane.showMessageDialog(null,mensaje);
            limpiar();
            listarEmpleado();
        }
    }//GEN-LAST:event_btn_AgregarActionPerformed

